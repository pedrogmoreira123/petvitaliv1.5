    private void BotaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoLoginActionPerformed
        String login, senha;
        login = campoLogin.getText();
        senha = campoSenha.getText();

        if (login.equals("adm") && senha.equals("adm")) {
            TelaMenu  telaMenu = new TelaMenu();
            telaMenu.setVisible(true);
            JOptionPane.showMessageDialog(null, "Seja Bem vindo Adm");
            dispose();
            
        } else if (login.equals("recp") && senha.equals("recp")) {
            TelaMenu  telaMenu = new TelaMenu();
            telaMenu.setVisible(true);
            JOptionPane.showMessageDialog(null,"bora que o dia sera longo");
            dispose(); 
        
        } else {
            JOptionPane.showMessageDialog(null,
                "Usuário e/ou Senha inválido!");
        }
    }//GEN-LAST:event_BotaoLoginActionPerformed

